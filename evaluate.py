def move_value(board: chess.Board, move: chess.Move, endgame: bool) -> float:
    if move.promotion is not None:
        return math.inf if board.turn == chess.WHITE else -math.inf

    movingPiece = board.piece_at(move.from_square)
    if movingPiece:
        fromValue = evaluate_piece(movingPiece, move.from_square, endgame)
        toValue = evaluate_piece(movingPiece, move.to_square, endgame)
        positionalValueChange = toValue - fromValue
    else:
        raise Exception(f'A piece was expected at {move.from_square}')

    captureValue = 0.0
    if board.is_capture(move):
        captureValue = evaluate_capture(board, move)

    totalMoveValue = captureValue + positionalValueChange

    if board.turn == chess.BLACK:
        totalMoveValue = -totalMoveValue

    return totalMoveValue
    

def evaluate_capture(board: chess.Board, move: chess.Move) -> float:
    if board.is_en_passant(move):
        return piece_value[chess.PAWN]
    capturedPiece = board.piece_at(move.to_square)
    capturingPiece = board.piece_at(move.from_square)
    if capturedPiece is None or capturingPiece is None:
        raise Exception(f'Pieces were expected at both {move.to_square} and {move.from_square}')
    return piece_value[capturedPiece.piece_type] - piece_value[capturingPiece.piece_type]


def evaluate_piece(piece: chess.Piece, square: chess.Square, end_game: bool) -> int:
    if piece.piece_type == chess.KING and end_game:
        return kingEvalEndGameWhite[square] if piece.color == chess.WHITE else list(reversed(kingEvalEndGameWhite))[square]
    
    return pieceEvalDict[piece.piece_type][square] if piece.color == chess.WHITE else list(reversed(pieceEvalDict[piece.piece_type]))[square]

def evaluate_board(board: chess.Board) -> float:
    total = 0
    end_game = check_end_game(board)

    for square in chess.SQUARES:
        piece = board.piece_at(square)
        if not piece:
            continue

        value = piece_value[piece.piece_type] + evaluate_piece(piece, square, end_game)
        total += value if piece.color == chess.WHITE else -value

    return total


def check_end_game(board: chess.Board) -> bool:
    queens = 0
    minors = 0

    for square in chess.SQUARES:
        piece = board.piece_at(square)
        if piece and piece.piece_type == chess.QUEEN:
            queens += 1
        if piece and (
            piece.piece_type == chess.BISHOP or piece.piece_type == chess.KNIGHT
        ):
            minors += 1

    if queens == 0 or (queens == 2 and minors <= 1):
        return True

    return False